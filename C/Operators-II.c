int main ()
{
int VariableA=1;
int VariableB=2;
  
printf("___________________\n");
printf("Operators Relational!!!\n");
printf("====================\n");
if( VariableA < VariableB)
{
    printf ("True\n");
    printf("Operators Relational ( < ). VariableA=1 < Variable=2 \n");
    printf("______________________________________________________\n");

}
else
    {
        printf("False\n");
        printf("Operators Relational ( < ). VariableA=1 < Variable=2 \n");
        printf("______________________________________________________\n");
    }
    

if( VariableA <= VariableB)
{
    printf ("True\n");
    printf("Operators Relational ( <= ). VariableA=1 <= Variable=2 \n");
    printf("______________________________________________________\n");

}
else
    {
        printf("False\n");
        printf("Operators Relational ( <= ). VariableA=1 <= Variable=2 \n");
        printf("______________________________________________________\n");
    }
    

    

if( VariableA > VariableB)
{
    printf ("True\n");
    printf("Operators Relational ( > ). VariableA=1 > Variable=2 \n");
    printf("______________________________________________________\n");

}
else
    {
        printf("False\n");
        printf("Operators Relational ( > ). VariableA=1 > Variable=2 \n");
        printf("______________________________________________________\n");
    }
    

    
     

if( VariableA >= VariableB)
{
    printf ("True\n");
    printf("Operators Relational ( >= ). VariableA=1 >= Variable=2 \n");
    printf("______________________________________________________\n");

}
else
    {
        printf("False\n");
        printf("Operators Relational ( >= ). VariableA=1 >= Variable=2 \n");
        printf("______________________________________________________\n");
    }
    

     

if( VariableA == VariableB)
{
    printf ("True\n");
    printf("Operators Relational ( == ). VariableA=1 == Variable=2 \n");
    printf("______________________________________________________\n");

}
else
    {
        printf("False\n");
        printf("Operators Relational ( == ). VariableA=1 == Variable=2 \n");
        printf("______________________________________________________\n");
    }
    

    

if( VariableA != VariableB)
{
    printf ("True\n");
    printf("Operators Relational ( != ). VariableA=1 != Variable=2 \n");
    printf("______________________________________________________\n");

}
else
    {
        printf("False\n");
        printf("Operators Relational ( != ). VariableA=1 != Variable=2 \n");
        printf("______________________________________________________\n");
    }
    

    
     
     
    
    
return 0;   
}
